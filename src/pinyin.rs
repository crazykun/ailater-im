/// Pinyin parser for input segmentation
pub struct PinyinParser {
    syllables: Vec<String>,
}

impl PinyinParser {
    /// Create a new pinyin parser
    pub fn new() -> Self {
        let syllables = get_pinyin_syllables()
            .iter()
            .map(|s| s.to_string())
            .collect();
        Self { syllables }
    }

    /// Parse input string into pinyin syllables
    /// Supports abbreviation (e.g., "cs" -> ["c", "s"])
    /// Uses greedy longest match algorithm
    pub fn parse(&self, input: &str) -> Vec<String> {
        let input = input.to_lowercase();
        let mut result = Vec::new();
        let mut pos = 0;
        let chars: Vec<char> = input.chars().collect();

        while pos < chars.len() {
            let mut found = false;
            // Try longest match first (up to 6 characters for pinyin syllables)
            for len in (1..=6.min(chars.len() - pos)).rev() {
                let segment: String = chars[pos..pos + len].iter().collect();
                if self.syllables.contains(&segment) {
                    result.push(segment);
                    pos += len;
                    found = true;
                    break;
                }
            }

            if !found {
                // Single character that's not a valid pinyin - treat as initial letter
                let ch = chars[pos];
                if ch.is_ascii_alphabetic() {
                    result.push(ch.to_string());
                }
                pos += 1;
            }
        }

        result
    }

    /// Check if a string is a valid pinyin syllable
    pub fn is_valid_syllable(&self, s: &str) -> bool {
        self.syllables.contains(&s.to_lowercase())
    }
}

impl Default for PinyinParser {
    fn default() -> Self {
        Self::new()
    }
}

/// Get candidate characters for a pinyin syllable
pub fn get_candidates(pinyin: &str) -> Vec<&'static str> {
    let map = get_pinyin_map();
    let initial_map = get_initial_map();
    let pinyin_lower = pinyin.to_lowercase();

    // First try direct lookup
    if let Some(candidates) = map.get(pinyin_lower.as_str()) {
        return candidates.clone();
    }

    // If it's a single character (initial letter), expand to all possible pinyin
    if pinyin_lower.len() == 1 {
        if let Some(chars) = pinyin_lower.chars().next() {
            if let Some(pinyins) = initial_map.get(&chars) {
                // Collect all candidates from all possible pinyin
                let mut all_candidates = Vec::new();
                for py in pinyins {
                    if let Some(candidates) = map.get(*py) {
                        all_candidates.extend(candidates.iter().copied());
                    }
                }
                return all_candidates;
            }
        }
    }

    Vec::new()
}

/// Fuzzy pinyin matching rules
pub struct FuzzyPinyinMatcher {
    /// Mapping of common fuzzy pinyin equivalences
    fuzzy_rules: Vec<(String, String)>,
}

impl FuzzyPinyinMatcher {
    pub fn new() -> Self {
        let fuzzy_rules = vec![
            // zh <-> z
            ("zh".to_string(), "z".to_string()),
            // ch <-> c
            ("ch".to_string(), "c".to_string()),
            // sh <-> s
            ("sh".to_string(), "s".to_string()),
            // an <-> ang
            ("an".to_string(), "ang".to_string()),
            // en <-> eng
            ("en".to_string(), "eng".to_string()),
            // in <-> ing
            ("in".to_string(), "ing".to_string()),
            // l <-> n
            ("l".to_string(), "n".to_string()),
            // f <-> h
            ("f".to_string(), "h".to_string()),
        ];
        Self { fuzzy_rules }
    }
    
    /// Get fuzzy matches for a pinyin syllable
    pub fn get_fuzzy_matches(&self, pinyin: &str) -> Vec<String> {
        let mut matches = vec![pinyin.to_lowercase()];
        
        for (from, to) in &self.fuzzy_rules {
            let pinyin_lower = pinyin.to_lowercase();
            if pinyin_lower.starts_with(from) {
                let fuzzy = format!("{}{}", to, &pinyin_lower[from.len()..]);
                if !matches.contains(&fuzzy) {
                    matches.push(fuzzy);
                }
            }
            if pinyin_lower.starts_with(to) {
                let fuzzy = format!("{}{}", from, &pinyin_lower[to.len()..]);
                if !matches.contains(&fuzzy) {
                    matches.push(fuzzy);
                }
            }
        }
        
        matches
    }
}

impl Default for FuzzyPinyinMatcher {
    fn default() -> Self {
        Self::new()
    }
}

